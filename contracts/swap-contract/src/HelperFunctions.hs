isValueInRange :: Integer -> Integer -> Integer -> Bool
isValueInRange value slippage target = lowEnd <= target && target <= highEnd
  where
    lowEnd :: Integer
    lowEnd = value - divide value slippage

    highEnd :: Integer
    highEnd = value + divide value slippage
